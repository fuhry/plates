			<h2>So what is a "plate"?</h2>
			
			<div class="well span4" style="float: right;">
				<h3>Why doesn't everybody call them "Garbage Plates"?</h3>
				<p>Because the original place that served them, Nick Tahou Hots, has the term Garbage Plate trademarked. So while lots of places serve the same dish, they all have their
					own creative names for it &mdash; Mark's, for example, calls it the "Sloppy Plate."</p>
			</div>
			
			<p>There is a local food up here in Rochester, NY we like to call the Garbage Plate.&trade; It's exactly what it sounds like: lots of food dumped on to a plate and served. Typical
				plate ingredients include French fries or homefries and macaroni salad as a base, topped with cheeseburgers or hots (hot dogs), hot sauce, mustard and/or onions on top, and
				French bread on the side.</p>
			<p>Plates are very localized to Rochester, but around Rochester they are served everywhere &mdash; there is even an upscale hotel, The Lodge at Woodcliff, which serves the "Plat
				de Refuse" for $15.75.
				
			<h3>Why do you call hot dogs "hots"?</h3>
			<p>Again a Rochester tradition. Rochester is home to Zweigle's, a local (but highly regarded nationally) hot dog and sausage manufacturer. They introduced the "white hot" (as it
				is known locally) in 1925.</p>
			
			<h3>Hot sauce? That's not what was on my plate!</h3>
			<p>While many people top their plates with traditional hot sauces (such as Frank's or Tabasco), in Rochester "hot sauce" refers to a meat sauce comparable to Cincinnati chili. It
				is typically made of ground beef with a spicy, fairly thin sauce.</p>
				
			<h3>Why did you make this site?</h3>
			<p>Contrary to what you might think by the name, plates are pretty hard to get right. We at <a href="http://www.csh.rit.edu/">Computer Science House</a> would like to think we've
				had a pretty good sampling of what's out there, and we want to share our findings.</p>
			
